  function getLevel() {
    return localStorage.getItem(LS_LEVEL_KEY) || 'level4';
  }

  function setLevel(level) {
    localStorage.setItem(LS_LEVEL_KEY, level);
  }

  // Lookup commission data for a product name (case-insensitive, trims whitespace)
  function lookup(baseProduct) {
    if (!baseProduct) return null;
    const key = baseProduct.toLowerCase().trim();
    if (DATA[key]) return DATA[key];
    // Partial match — find entry whose key is contained in the product name or vice versa
    for (const [k, v] of Object.entries(DATA)) {
      if (key.includes(k) || k.includes(key)) return v;
    }
    return null;
  }

  // Returns the round-trip commission in EUR for one trade
  // contracts = number of lots; commission = rate × contracts × 2 (round trip)
  function getCommissionEUR(baseProduct, contracts) {
    const entry = lookup(baseProduct);
    if (!entry) return 0;
    const lvlIdx = LEVEL_INDEX[getLevel()] ?? 3;
    const rate   = entry.rates[lvlIdx];
    const native = rate * (contracts ?? 1) * 2;
    return FX.toEUR(native, entry.currency);
  }

  // Sets commissionEUR and netPnlEUR on each trade in-place
  function applyToTrades(trades) {
    for (const t of trades) {
      if (t.isOpen) { t.commissionEUR = 0; t.netPnlEUR = t.pnlEUR; continue; }
      t.commissionEUR = getCommissionEUR(t.baseProduct, t.totalContracts);
      t.netPnlEUR = t.pnlEUR !== null ? t.pnlEUR - t.commissionEUR : null;
    }
  }

  return { applyToTrades, getCommissionEUR, lookup, getLevel, setLevel, LEVEL_INDEX };
