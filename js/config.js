// Lookup by base product name — user config takes priority over built-in defaults
function getContractSpec(baseProduct) {
  // 1. User-saved override (from Settings tab)
  try {
    const userCfg = JSON.parse(localStorage.getItem('ta_product_config') || '{}');
    if (userCfg[baseProduct]) return userCfg[baseProduct];
  } catch {}

  // 2. Exact match in built-in table
  if (CONTRACT_SPECS[baseProduct]) return CONTRACT_SPECS[baseProduct];

  // 3. Fuzzy match — key must be at least 6 chars to avoid spurious short matches
  const lower = baseProduct.toLowerCase();
  for (const [key, spec] of Object.entries(CONTRACT_SPECS)) {
    if (key.length < 6) continue;
    if (lower.includes(key.toLowerCase()) || key.toLowerCase().includes(lower)) {
      return spec;
    }
  }

  // 4. Fallback — unknown product
  return { currency: 'USD', multiplier: 1, assetClass: 'Other' };
}

