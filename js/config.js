// Strips contract month suffix e.g. "ICE LS Gasoil JUN26" -> "ICE LS Gasoil"
function stripContractMonth(product) {
  return product.replace(/\s+(JAN|FEB|MAR|APR|MAY|JUN|JUL|AUG|SEP|OCT|NOV|DEC)\d{2}$/i, '').trim();
}

